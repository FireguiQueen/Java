// Métodos, parâmetros e argumentos
/*
    Um método é um bloco que possui um nome próprio. Este bloco conterá diversas instruções.
    Assim que este método é invocado, essas instruções serão executadas. 

    Um método pode receber inputs (valores de entrada). Para isso, o método precisa possuir parâmetros,
    e esses parâmetros são utilizados diretamente em nosso bloco. 
    Quando invocarmos este método, passaremos os argumentos, que por sua vez, são valores que substituiram 
    esses parâmetros que estão dentro do bloco.

    Esses parâmetros são utilizados
    É útil para:
        - Efiência
        - Beleza do código 
        - Praticidade
*/


// Cabeçalho 
/*
    O cabeçalho, ou em inglês, "header", é a parte onde declaramos o nosso método.
    Nele conterá:
    - modificadores: public static
    - tipo de retorno: void, int, double, string.. 
    - nome do método: (qualquer nome que a gente quiser)
    - parâmetros: (são as variáveis sem valores, que ao invocarmos os métodos, terão valores atrubuidos a elas devido aos argumentos)
*/
public static int retorna1()
{
    return 1; 
}


// Corpo do método 
/*
    O corpo do método nada mais é do que as instruções que são contidas dentro deste método.
    Eles ficam entre duas chaves {}. 
*/


// Tipo de retorno
/*
    O tipo de retorno de um método é o valor que ele irá retornar pela palavra chave "return".
    > Se o nosso método precisa retornar um valor inteiro, usaremos um método do tipo "int";
    > Se o nosso método precisa retornar um valor inteiro mais longo, usaremos um método do tipo "long";
    > Se o nosso método precisa retornar um valor quebrado, usaremos um método do tipo "double";
    > Se o nosso método precisa retornar uma string, usaremos um método do tipo "string".

    > Se o nosso método NÃO precisa retornar NENHUM VALOR, podemos colocar um método do tipo "void".
    -- O void é sempre utilizado no nosso método 'main', pois ele não retorna nada, apenas executa 
    -- instruções.
*/
    


