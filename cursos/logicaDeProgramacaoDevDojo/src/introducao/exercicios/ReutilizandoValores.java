// %: 30, 15 and 5.

public class ReutilizandoValores
{
    public static void main(String[] args)
    {
        double valorInicial = 900;

        double resultado = valorInicial * 0.30;
        System.out.println("30% de " + valorInicial + " é: " + resultado);

        resultado = valorInicial * 0.15;
        System.out.println("15% de " + valorInicial + " é: " + resultado);

        resultado = valorInicial * 0.05;
        System.out.println("5% de " + valorInicial + " é: " + resultado);
    }
}
